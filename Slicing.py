#SLICING
name="Meru University"
print(name)#prints the whole name
print(name[0])#prints the character on the 0 index
print(name[2:5])#prints from the 2nd character to the 4th one,,,5th not included
print(name[2:])#prints from 2nd character to the last  one
print(name * 2)#prints the name 2 times
print(name+"Library")#concatenation
print(name[::2])#slicing with steps
